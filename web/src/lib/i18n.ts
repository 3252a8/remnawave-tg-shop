export function t(locale: LocaleCode | string | null | undefined, key: keyof typeof messages.ru | string, vars: Record<string, string | number> = {}): string {
  const lang = (locale === 'ru' ? 'ru' : 'en') as LocaleCode;
  const current = messages[lang] as Record<string, string>;
  const fallback = messages.en as Record<string, string>;
  const template = current[String(key)] ?? fallback[String(key)] ?? String(key);
  return template.replace(/\{(\w+)\}/g, (_match: string, name: string) => String(vars[name] ?? ''));
}
